# Polygon to Raster (under the hood)
# In short, if overlapping polygons, tool will supersede using the value of the polygon that takes up the most space within the cell.
# If overlapping polygons take up the same amount of space, it will take the higher value of the priority field. As such CATZOC/YEAR were
# combined such that (10000*(6-CATZOC))+Year to achieve the maximum combination of Year and CATZOC

# MAXIMUM_COMBINED_AREA METHOD
# For this description, the combined feature is considered to be a single feature with the FID equal to the lowest of the
# combined features and the priority equal to the highest of the combined features.

# If no priority is specified, and
    # Only one feature overlaps the cell, it must overlap by at least 50% to be selected.
    # Only one feature has the largest area, then it is selected.
    # More than one feature has the same size area, then the one with the lowest FID is selected.
# If priority is specified, and;
    # The feature in question overlaps the cell by less than 50%, and
    # The priority is greater than zero, then it is considered.
    # The priority is equal to zero, then the feature is not considered.
    # Only one feature has the largest area, then it is selected.
    # More than one feature has the same largest area, and
    # Their priorities are different, then the one with the highest priority is selected.
    # Their priorities are the same, then the one with the smallest FID is selected.
    # The feature boundary coincides with a cell boundary, then the boundary rules apply. '''
