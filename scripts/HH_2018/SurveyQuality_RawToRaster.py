# CATZOC
# Data Source
#     1. HCELL CATZOC Layer, compiled from M_QUAL values stored within HCELLs on HSD Data Archive (Z:/Products/Data Archive)
#     2. MCD CATZOC Layer, compiled by Sean Legeer. (second authority) and represents data applied to the chart
#             - This layer includes three sources
#                     1. ENC
#                     2. Source (HCELL --> Source), awaiting ENC application
#                     3. 2013 Superseeded CATZOC layer that has been extensively QC'd relative to ENC Chart Histories and data available on NCEI
#                             This layer does not include F, W, or D surveys and used the following classification
#                                 Survey Type                                                           # CATZOC
#                                 Complete MBES                                                         # A1
#                                 200% SSS with concurrent MBES                                         # A1
#                                 100 % SSS with complete MBES                                          # A1
#                                 SSS (no MBES)                                                         # A2
#                                 Bathy Lidar                                                           # B
#                                 SBES Nearshore (1:40k or larger) since 1940                           # B
#                                 SBES Offshore since 1940                                              # C
#                                 1920 - 1940 surveys on known horizontal and vertical datums           # C
#                                 Pre 1940 surveys with unknown horizontal and vertical datums          # D
#                                 All surveys pre-1920                                                  # D
#             The following assumptions were made:
#                 If CATZOC = 6, then deleted from database. These areas will default to the HSD OPS CATZOC Assessment Layer
#                         Talking with Sean Legeer, CATZOC = 6 denoted (1) foreign waters for which we do not have a CATZOC value (2) Navy-only data with no CATZOC and (3) Dumping ground
#                 If CATZOC = 5 and Year = NALL, then year set to 1919.
#                         Talking with Sean Legeer, these areas are purposefully set to Year = NALL as the year of source is expected to be before 1920 (re 2013 classification). The majority of this area covers inland water ways. If HSD OPS dataset was used to replace these areas, they would likely receive a CATZOC U with no year, which is less desirable.
#                 If CATZOC = 3 and Year = NALL, then assumed to be USACE areas (following direction from Sean Legeer).
#                         CATZOC = 3/Year = NALL is merged with DREGARE and ARCHARE features from ENC Direct to extract SORDAT. Any areas where SORDAT = NULL are deleted from the database and the area will default to HSD OPS CATZOC Layer
#                 If CATZOC <> 6, 5, 3 and Year = NALL, then deleted from database. These areas will default to the HSD OPS CATZOC Assessment Layer
#
#     3. SURDEX CATZOC Assessment
#             - This layer includes two sources and is only applied in those areas where the MCD CATZOC layer has no data
#                     TECSOU/Chart Scale/Year Algorithm
#                     Note: All W and D surveys were increased by 1 (e.g. If CATZOC B, per below algorithm, and W or D survey, then CATZOC C)
#                     If TECSOU was blank, assumptions were made based on year:
#                         If year >1940 and <2000, TECSOU = 1
#                         If year >2000, TECSOU = 1, 3 (prevents assumption from being catzoc 1)
#                     OD/CC (A1)
#                     Year < 1920   (D)
#                     (TECSOU contains 3 OR TECSOU contains 1, 3) and year > 2000 AND TECSOU was not originally blank   (A2)
#                     (TECSOU contains 3 OR TECSOU contains 1, 3) and year > 2000 AND TECSOU was originally blank   (B)
#                     (TECSOU contains 2) AND (1 OR 3)  (A2)
#                     TECSOU = 2 AND Year < 2008    (C)
#                     TECSOU = 2 AND Year >= 2008   (B)
#                     (TECSOU contains 1 or 8), year > 1940, scale <= 40000     (B)
#                     (TECSOU contains 1 or 8), year > 1940, scale > 40,000     (C)
#                     (TECSOU contains 7 and 3) OR (TECSOU = 7 AND Year >= 2014)    (B)
#                     TECSOU contains 3 (B)
#                     Everything Else   (C)
# Notes:
# This script also joins the OD-classified surveys with SURDEX. The OD-classified surveys was generated by (1) reviewing DRS vs assigned coverage OR (2) reviewing PIs and assuming coverage requirements were met.
# This OD layer is used if the superseded CATZOC value is equal to 1.

